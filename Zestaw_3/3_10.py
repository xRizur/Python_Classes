keys = ["1", "5", "10", "50", "100", "500", "1000"]
values = ["I", "V", "X", "L", "C", "D", "M"]
D = dict(zip(keys, values))
assert D == {'1': 'I', '5': 'V', '10': 'X', '50': 'L', '100': 'C', '500': 'D', '1000': 'M'}

E = {}
for (k, v) in zip(keys, values):
    E[k] = v
assert E == {'1': 'I', '5': 'V', '10': 'X', '50': 'L', '100': 'C', '500': 'D', '1000': 'M'}